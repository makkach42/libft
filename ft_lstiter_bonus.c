#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*ptr;

	if (!lst || !f)
		return ;
	ptr = lst;
	while (ptr != 0)
	{
		(*f)(ptr->content);
		ptr = ptr->next;
	}
}
